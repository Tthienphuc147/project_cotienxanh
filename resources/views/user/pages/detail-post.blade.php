                <div class="content-post">

                    <p>&nbsp;</p>

                    <p><strong>Trẻ từ 1 đến 3 tuổi ph&aacute;t triển nhanh cả về thể lực v&agrave; tr&iacute; tuệ, nếu được chǎm s&oacute;c, nu&ocirc;i dưỡng đầy đủ sẽ ph&aacute;t triển tốt, &iacute;t ốm đau bệnh tật. Bộ m&aacute;y ti&ecirc;u h&oacute;a v&agrave; c&aacute;c chức nǎng ti&ecirc;u h&oacute;a, hấp thu của trẻ chưa thật ho&agrave;n chỉnh n&ecirc;n c&aacute;c thiếu s&oacute;t trong nu&ocirc;i dưỡng v&agrave; chǎm s&oacute;c trẻ c&oacute; thể dẫn đến suy dinh dưỡng. N&ecirc;n tận dụng sữa mẹ để hỗ trợ th&ecirc;m cho b&eacute; dinh dưỡng v&agrave; kh&aacute;ng thể, cố gắng cho trẻ b&uacute; đến 18 - 24 th&aacute;ng. Khẩu phần ǎn của trẻ cần được cung cấp đủ nǎng lượng, chất đạm, chất b&eacute;o, vitamin v&agrave; muối kho&aacute;ng.</strong></p>

                    <p><strong><img alt="" src="https://files.benhvien108.vn/untitleddd.jpg" /></strong></p>

                    <p><br />
                    <br />
                    <strong>1. Nǎng lượng</strong>: Cung cấp đủ năng lượng gi&uacute;p trẻ ph&aacute;t triển to&agrave;n diện, sự ti&ecirc;u hao nǎng lượng của trẻ lớn do trẻ chơi, đ&ugrave;a nghịch nhiều. Nhu cầu nǎng lượng ở lứa tuổi n&agrave;y l&agrave; 100-110Kcal/kg c&acirc;n nặng, trẻ nặng khoảng 9 - 13 kg th&igrave; nǎng lượng cung cấp l&agrave; 900 - 1300 kcal. Nǎng lượng được cung cấp đủ qua bữa ǎn của trẻ gồm c&oacute;: Chất bột như bột, ch&aacute;o, chất đạm, chất b&eacute;o, sữa... Tỷ lệ giữa c&aacute;c th&agrave;nh phần sinh nǎng lượng n&ecirc;n l&agrave;: Đạm: B&eacute;o: Đường bột = 15: 20: 65.</p>

                    <p><br />
                    <strong>2. Chất đạm</strong>: Chất đạm rất cần cho sự ph&aacute;t triển cơ thể trẻ, cần ưu ti&ecirc;n c&aacute;c loại đạm động vật c&oacute; gi&aacute; trị cao, đủ acid amin, gi&agrave;u c&aacute;c yếu tố vi lượng như sắt, kẽm, vitamin: thịt, sữa, trứng, c&aacute;, t&ocirc;m&hellip;. Lượng đạm động vật trong khẩu phần ǎn của trẻ n&ecirc;n đạt từ 50 - 60%. N&ecirc;n phối hợp tốt đạm động vật với đạm thực vật (đậu đỗ, vừng, lạc...) sẽ tạo được sự c&acirc;n đối gi&uacute;p hấp thu v&agrave; sử dụng đạm tốt hơn. Nhu cầu chất đạm l&agrave; 35-44g/ ng&agrave;y. Chế độ ǎn thiếu đạm sẽ l&agrave;m cho trẻ suy dinh dưỡng, chậm lớn, k&eacute;m th&ocirc;ng minh, nhưng nếu trẻ ǎn qu&aacute; nhiều đạm sẽ l&agrave; g&aacute;nh nặng cho gan, thận. Mặt kh&aacute;c, trong qu&aacute; tr&igrave;nh ti&ecirc;u h&oacute;a chất đạm tạo ra nhiều sản phẩm g&acirc;y thối rữa, độc hại.&nbsp;</p>

                    <p><br />
                    <strong>3. Chất b&eacute;o:&nbsp;</strong>Dầu mỡ cung cấp nǎng lượng cao, l&agrave;m tǎng cảm gi&aacute;c ngon miệng lại gi&uacute;p trẻ hấp thu v&agrave; sử dụng tốt c&aacute;c vitamin tan trong chất b&eacute;o như vitamin A, D, K, E. Mỗi b&aacute;t bột, b&aacute;t ch&aacute;o cần cho th&ecirc;m 1 -2 th&igrave;a c&agrave; ph&ecirc; mỡ hoặc dầu. Mỡ lợn, mỡ g&agrave; c&oacute; c&aacute;c axit b&eacute;o kh&ocirc;ng no cần thiết như: axit linoleic, axit arachidonic rất cần thiết cho qu&aacute; tr&igrave;nh ph&aacute;t triển của trẻ, nhất l&agrave; n&atilde;o bộ. Nếu trẻ đ&atilde; ǎn cơm th&igrave; n&ecirc;n cho mỡ hoặc dầu v&agrave;o x&agrave;o, r&aacute;n, kho với thức ǎn để trẻ ǎn được (20-40g dầu mỡ/ ng&agrave;y)</p>

                    <p><br />
                    <strong>4. C&aacute;c chất kho&aacute;ng</strong>:&nbsp;C&oacute; vai tr&ograve; quan trọng trong sự tạo xương, tạo rǎng, tạo m&aacute;u v&agrave; c&aacute;c hoạt động chức nǎng sinh l&yacute; của cơ thể. Can xi v&agrave; photpho cần được ch&uacute; &yacute; để cung cấp đủ cho trẻ, khoảng 500 - 600mg/ng&agrave;y. Canxi c&oacute; nhiều trong sữa v&agrave; c&aacute;c loại nhuyễn thể (t&ocirc;m, cua, ốc, trai...). Photpho c&oacute; nhiều trong c&aacute;c loại lương thực, ngũ cốc. Tỷ lệ tốt nhất giữa canxi/ photpho = 1/1,5-1/1,8. Chuyển h&oacute;a canxi v&agrave; photpho trong cơ thể được điều h&ograve;a bởi vitamin D, vitamin D lại c&oacute; trong l&ograve;ng đỏ trứng, thịt, gan v&agrave; dưới t&aacute;c dụng của &aacute;nh nắng mặt trời tiền vitamin D dưới dạng dự trữ dưới da sẽ chuyển th&agrave;nh vitamin D hoạt động. Do vậy ngo&agrave;i ǎn uống đủ, cần cho trẻ ra ngo&agrave;i tắm nắng mỗi ng&agrave;y khoảng 15-20 ph&uacute;t trước 8h s&aacute;ng.</p>

                    <p>Chất sắt tham gia v&agrave;o qu&aacute; tr&igrave;nh tạo m&aacute;u. Mỗi ng&agrave;y trẻ cần 7-8 mg/ng&agrave;y thức ǎn. Nguồn sắt tốt c&oacute; trong thức ǎn động vật l&agrave; c&aacute;c nội tạng: tim, gan, bầu dục, ở thực vật l&agrave; đậu đỗ v&agrave; c&aacute;c loại rau c&oacute; m&agrave;u xanh sẫm: rau muống, rau ng&oacute;t. Sắt trong thức ǎn động vật hấp thu tốt hơn trong thức ǎn thực vật. Vitamin C trong rau gi&uacute;p cơ thể hấp thu v&agrave; sử dụng sắt c&oacute; hiệu quả hơn.</p>

                    <p>Kẽm l&agrave; 1 vi chất dinh dưỡng ảnh hưởng đến qua tr&igrave;nh tăng trưởng v&agrave; ti&ecirc;u h&oacute;a. Trẻ em thiếu kẽm thường c&ograve;i cọc, hay mắc c&aacute;c bệnh nhiễm khuẩn về đường h&ocirc; hấp, n&ocirc;n trớ, rối loạn giấc ngủ&hellip; Nhu cầu kẽm khoảng 8-10mg/ng&agrave;y. Kẽm c&oacute; nhiều trong c&aacute;c loại thức ăn động vật như: hải sản, trai, hến, s&ograve; huyết, thịt c&aacute;&hellip;</p>

                    <p>&nbsp;</p>

                    <p><strong>5. Vitamin</strong><strong>: &nbsp;</strong>Lứa tuổi n&agrave;y người ta quan t&acirc;m đến vitamin A, D, C, ch&uacute;ng cần cho sự ph&aacute;t triển xương, răng, sự tạo m&aacute;u, tǎng sức đề kh&aacute;ng cho cơ thể. Nhu cầu Vitamin A: 400-450 mcg/ng&agrave;y, Vitamin D 400UI/ng&agrave;y, Vitamin C: 30mg/ng&agrave;y, Vitamin A c&oacute; trong c&aacute;c trứng, gan. . . Rau quả c&oacute; m&agrave;u v&agrave;ng, đỏ, da cam vừa l&agrave; nguồn cung cấp caroten (tiền vitamin A) vừa l&agrave; nguồn cung cấp vitamin C. Để đảm bảo nhu cầu vitamin cần cho trẻ ǎn rau, quả thường xuy&ecirc;n.</p>

                    <p>&nbsp;</p>

                    <p><strong><strong>Những điểm cần lưu &yacute; trong nu&ocirc;i dưỡng trẻ từ 1 đến 3 tuổi:&nbsp;</strong></strong></p>

                    <p>- Thức ǎn cần chế biến từ mềm đến cứng, từ &iacute;t đến nhiều để trẻ quen dần. Khi trẻ đ&atilde; mọc rǎng h&agrave;m, cần tạo điều kiện cho trẻ luyện rǎng, luyện cơ nhai, do vậy kh&ocirc;ng cần thiết phải cho mọi thức ǎn v&agrave;o m&aacute;y xay sinh tố nghiền n&aacute;t m&agrave; n&ecirc;n th&aacute;i, bǎm từ rất nhỏ đến nhỏ vừa; nấu từ rất mềm đến mềm vừa đến cứng để tạo cảm gi&aacute;c ngon miệng v&agrave; gi&uacute;p rǎng lợi, cơ nhai, cơ ti&ecirc;u h&oacute;a ph&aacute;t triển.&nbsp;</p>

                    <p>- Sau khi cai sữa cần c&oacute; chế độ ǎn ri&ecirc;ng cho trẻ, kh&ocirc;ng bắt trẻ ǎn chung qu&aacute; sớm với người lớn sẽ ảnh hường tới ti&ecirc;u h&oacute;a của trẻ, đặc biệt l&agrave; lượng muối trong thức ăn.</p>

                    <p>- Thường xuy&ecirc;n thay đổi c&aacute;ch chế biến để tạo cảm gi&aacute;c ngon miệng.&nbsp;</p>

                    <p>- Hạn chế cho trẻ ǎn đồ ngọt (đường, b&aacute;nh kẹo). Đường ngọt l&agrave;m cho trẻ c&oacute; cảm gi&aacute;c no giả tạo n&ecirc;n kh&ocirc;ng muốn ǎn c&aacute;c thức ǎn kh&aacute;c, mặt kh&aacute;c n&oacute; c&ograve;n ứ lại trong miệng rồi chuyển th&agrave;nh axit dễ l&agrave;m hỏng rǎng. Chỉ n&ecirc;n cho trẻ ǎn b&aacute;nh, kẹo sau bữa ǎn.&nbsp;</p>

                    <p>- Cần cho trẻ uống đủ nước: 100ml/kg/ng&agrave;y, nước gi&uacute;p trẻ ti&ecirc;u h&oacute;a v&agrave; hấp thu tốt c&aacute;c loại dinh dưỡng, nước c&ograve;n c&oacute; vai tr&ograve; vận chuyển gi&uacute;p thải trừ c&aacute;c sản phẩm cặn b&atilde;, độc hại của qu&aacute; tr&igrave;nh chuyển h&oacute;a ra khỏi cơ thể.&nbsp;</p>

